void main() {
  // INT
  int num = 100;
  print(num);
  print(num.runtimeType);

  // Int To double
  print(num.toDouble());
  print(num.toDouble().runtimeType);

  // Int to String
  print(num.toString());
  print(num.toString().runtimeType);

  print("\n---------------\n");

  // double
  double num2 = 403.543;
  print(num2);
  print(num2.runtimeType);

  // String
}
